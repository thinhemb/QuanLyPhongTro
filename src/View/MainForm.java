        txtdientich.setEnabled(true);
        txtsonguoi.setEnabled(true);
        txtgiathue.setEnabled(true);
        cbNam.setEnabled(true);
        cbNu.setEnabled(true);
        cbtinhtrang.setEnabled(true);
